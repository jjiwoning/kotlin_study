package algo.boj.P2754

fun main() {
    val s = readln()

    if (s == "A+") {
        println(4.3)
        return
    }

    if (s == "A0") {
        println(4.0)
        return
    }

    if (s == "A-") {
        println(3.7)
        return
    }

    if (s == "B+") {
        println(3.3)
        return
    }

    if (s == "B0") {
        println(3.0)
        return
    }

    if (s == "B-") {
        println(2.7)
        return
    }

    if (s == "C+") {
        println(2.3)
        return
    }

    if (s == "C0") {
        println(2.0)
        return
    }

    if (s == "C-") {
        println(1.7)
        return
    }

    if (s == "D+") {
        println(1.3)
        return
    }

    if (s == "D0") {
        println(1.0)
        return
    }

    if (s == "D-") {
        println(0.7)
        return
    }

    if (s == "F") {
        println(0.0)
        return
    }

}